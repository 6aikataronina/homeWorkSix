public class GameEntity {
    private int Hp;
    private int Dmg;
    private String name;

    public int getHp() {
        return Hp;
    }

    public void setHp(int hp) {
        Hp = hp;
    }

    public int getDmg() {
        return Dmg;
    }

    public void setDmg(int dmg) {
        Dmg = dmg;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
